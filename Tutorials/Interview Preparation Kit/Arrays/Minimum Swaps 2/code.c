// Complete the minimumSwaps function below.
int minimumSwaps(int arr_count, int* arr) {
    int c = 0;
    int index[arr_count];

    for (int i = 0; i < arr_count;i++) { // create a dictionary that returns the index
        index[arr[i] - 1] = i;
    }

    for (int i = 0; i < arr_count - 1; i++) {
        if (arr[i] != i + 1) {
        c++;

        // swap arr
        int t_arr = arr[i];
        arr[i] = i + 1;
        arr[index[i]] = t_arr;

        // swap index
        int t = index[i];
        index[i] = i;
        index[t_arr - 1] = t;
        }
    }

    return c;
}

